public class Ex09_02 {

	public static void main(String[] args) {
		
		System.out.println( 3 + 5);	// 8   + 덧셈연산자
		System.out.println( "3" + "5");	// "35"   + 문자열연결연산자
		

		System.out.println( "3" + 5);// "35"	+ 문자열연결연산자
		System.out.println( "3" + true);// "3true"	+ 문자열연결연산자
		System.out.println( "3" + 0.14);// "30.14"	+ 문자열연결연산자
	}

}






